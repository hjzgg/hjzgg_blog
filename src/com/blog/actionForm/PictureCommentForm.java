package com.blog.actionForm;

public class PictureCommentForm {
	private String groupId;
	private String pictureCommentContent;
	private String commentPeopleContact;
	public String getGroupId() {
		return groupId;
	}
	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}
	public String getPictureCommentContent() {
		return pictureCommentContent;
	}
	public void setPictureCommentContent(String pictureCommentContent) {
		this.pictureCommentContent = pictureCommentContent;
	}
	public String getCommentPeopleContact() {
		return commentPeopleContact;
	}
	public void setCommentPeopleContact(String commentPeopleContact) {
		this.commentPeopleContact = commentPeopleContact;
	}
	
}
